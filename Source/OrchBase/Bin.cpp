#include "Bin.h"

//==============================================================================

Bin::Bin(FILE * b) : Binary(b)
{
}

//------------------------------------------------------------------------------

Bin::~Bin()
{
if (Binary) fclose(Binary);
}

//------------------------------------------------------------------------------

void Bin::Dump(unsigned off,FILE * fp)
{
string s(off,' ');
const char * os = s.c_str();
fprintf(fp,"\n%sBin ++++++++++++++++++++++++++++++++++++++++++++++++++++\n",os);

fprintf(fp,"%sBin ----------------------------------------------------\n\n",os);
fflush(fp);
}

